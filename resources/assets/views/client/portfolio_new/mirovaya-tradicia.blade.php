                        и неписаном правиле — «хлеб выбрасывать нельзя». Злаки — дают жизнь, а пшеница —
                        священный элемент русской культуры. Трепетное отношение к хлебу и традиционный
                        метод изготовления водки легли в основу эмоционального посыла «Мировой Традиции».
